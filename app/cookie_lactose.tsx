import { Feather } from "@expo/vector-icons";
import { NavigationProp, useNavigation } from "@react-navigation/native";
import React, { useState } from "react";
import {
    Alert,
    Image,
    ScrollView,
    StyleSheet,
    Text,
    TouchableOpacity,
    View,
} from "react-native";

type CheckedItems = {
  [key: string]: boolean;
};

export default function CookiesDeChocolate() {
  const nav = useNavigation<NavigationProp<any>>();

  const [checkedItems, setCheckedItems] = useState<CheckedItems>({
    item1: false,
    item2: false,
    item3: false,
    item4: false,
    item5: false,
    item6: false,
    item7: false,
    item8: false,
    step1: false,
    step2: false,
    step3: false,
    step4: false,
    step5: false,
    step6: false,
  });

  const itemsMap: { [key: string]: string } = {
    item1: "2 colheres (sopa) de óleo.",
    item2: "1/2 xícara de açúcar mascavo.",
    item3: "1 colher (chá) de fermento.",
    item4: "3/4 de xícara de farinha de trigo.",
    item5: "3/4 xícara de açúcar.",
    item6: "1 ovo.",
    item7: "3 colheres de cacau em pó (opcional).",
    item8: "1 xícara de granola.",
  };

  const stepsMap: { [key: string]: string } = {
    step1:
      "Misture o óleo, o açúcar mascavo, o açúcar (e o chocolate em pó se for fazer cookies de chocolate).",
    step2: "Adicione o ovo e misture bem.",
    step3: "Acrescente a farinha aos poucos e misture bem.",
    step4:
      "Adicione o fermento e misture de leve, só para o fermento incorporar à massa.",
    step5: "Por último, acrescente a granola.",
    step6:
      "Forme bolinhas sobre o papel manteiga e leve ao forno preaquecido a 250ºC por aproximadamente 20 minutos.",
  };

  const toggleCheck = (item: string) => {
    setCheckedItems((prev) => ({
      ...prev,
      [item]: !prev[item],
    }));
  };

  return (
    <ScrollView showsVerticalScrollIndicator={false} style={{ flex: 1 }}>
      <View style={styles.container}>
        {/* Imagem decorativa (adicione a sua imagem no assets e ajuste caminho) */}
        <Image
          source={require("../assets/images/fundo_cookieslactose.png")} // troque pela sua imagem de fundo para cookies
          style={styles.decorativeImage}
          resizeMode="contain"
        />

        <View style={styles.tituloContainer}>
          <TouchableOpacity onPress={() => nav.navigate("restricoes")}>
            <Feather name="chevron-left" size={28} color="#000" />
          </TouchableOpacity>
          <Text style={styles.paragraph}>COOKIES DE CHOCOLATE</Text>
        </View>

        <Text style={styles.ingredientes}>INGREDIENTES</Text>
        <View style={styles.ingredientesContainer}>
          <View>
            {Object.entries(itemsMap).map(([key, item]) => (
              <TouchableOpacity key={key} onPress={() => toggleCheck(key)}>
                <Text style={styles.topicos}>
                  {checkedItems[key] ? (
                    <Text style={styles.check}>✓ </Text>
                  ) : (
                    <Text style={styles.bolinha}>⚪ </Text>
                  )}
                  {item}
                </Text>
              </TouchableOpacity>
            ))}
          </View>
        </View>

        <Text style={styles.ingredientes}>MODO DE PREPARO</Text>
        {Object.entries(stepsMap).map(([key, step]) => (
          <TouchableOpacity key={key} onPress={() => toggleCheck(key)}>
            <Text style={styles.topicos}>
              {checkedItems[key] ? (
                <Text style={styles.check}>✓ </Text>
              ) : (
                <Text style={styles.bolinha}>⚪ </Text>
              )}
              {step}
            </Text>
          </TouchableOpacity>
        ))}

        <View style={styles.botoesContainer}>
          <TouchableOpacity
            style={styles.botaoVerde}
            onPress={() => Alert.alert("Forma correta descarte")}
          >
            <Feather
              name="refresh-cw"
              size={20}
              color="#fff"
              style={styles.iconeBotao}
            />
            <Text style={styles.textoBotao}>Forma correta descarte</Text>
          </TouchableOpacity>
          <TouchableOpacity
            style={styles.botaoCinza}
            onPress={() => Alert.alert("Função em desenvolvimento")}
          >
            <Feather
              name="download"
              size={20}
              color="#FFCC00"
              style={styles.iconeBotao}
            />
            <Text style={styles.textoBotao}>Baixar lista de compra</Text>
          </TouchableOpacity>
        </View>
      </View>
    </ScrollView>
  );
}

